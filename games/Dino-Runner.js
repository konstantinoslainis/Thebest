// --- GAME VARIABLES ---
let isJumping = false;
let jumpFrame = 0;
let duckTimer = 0;
let gameOver = false;
let rawScore = 0;
let highScore = 0; 
let flashTimer = 0;
let gameTimer; 
let spawnTimer = 0;

// NEW: Background Music Variables
let isMusicPlaying = false;
let bgmLoop = null; 

const jumpArc = [-1, -2, -3, -3, -3, -2, -1, 0]; 
const groundY = 4; 

function startGame() {
  clearTimeout(gameTimer); 
  
  gameOver = false;
  rawScore = 0;
  flashTimer = 0;
  isJumping = false;
  jumpFrame = 0;
  duckTimer = 0;
  spawnTimer = 3; 
  
  // Reset music state on restart, but don't play it yet!
  if (bgmLoop) {
    bgmLoop.end();
    isMusicPlaying = false;
  }
  
  let d = getFirst(d1) || getFirst(d2) || getFirst(u1) || getFirst(u2);
  if (d) {
    d.type = d1;
    d.y = groundY;
  }

  [c1, c2, b1, b2, h1, h2, cloud].forEach(type => {
    getAll(type).forEach(e => e.remove());
  });

  tick();
}

function padScore(score) {
  let str = score.toString();
  while (str.length < 5) str = "0" + str;
  return str;
}

function tick() {
  if (gameOver) return;
  
  rawScore += 1;
  let displayScore = Math.floor(rawScore / 5);
  
  if (displayScore > highScore) {
    highScore = displayScore;
  }
  
  clearText();
  
  // Milestone Sound & Flashing
  if (displayScore > 0 && displayScore % 100 === 0 && rawScore % 5 === 0) {
    flashTimer = 15; 
    playSound(sfxMilestone); 
  }

  let scoreText = "HI " + padScore(highScore) + "  " + padScore(displayScore);

  if (flashTimer > 0) {
    flashTimer--;
    if (flashTimer % 2 === 0) addText(scoreText, { x: 3, y: 0, color: color`0` });
  } else {
    addText(scoreText, { x: 3, y: 0, color: color`0` });
  }

  let d = getFirst(d1) || getFirst(d2) || getFirst(u1) || getFirst(u2);
  if (!d) return;

  let animTick = Math.floor(rawScore / 4) % 2 === 0;

  if (isJumping) {
    if (jumpFrame < jumpArc.length) {
      d.y = groundY + jumpArc[jumpFrame];
      d.type = duckTimer > 0 ? u1 : d1; 
      jumpFrame++;
    } else {
      d.y = groundY; 
      isJumping = false;
      jumpFrame = 0;
    }
  } else {
    if (duckTimer > 0) {
      duckTimer--;
      d.type = animTick ? u1 : u2; 
    } else {
      d.type = animTick ? d1 : d2; 
    }
  }

  getAll(b1).concat(getAll(b2)).forEach(b => b.type = animTick ? b1 : b2);
  getAll(h1).concat(getAll(h2)).forEach(b => b.type = animTick ? h1 : h2);

  [c1, c2, b1, b2, h1, h2].forEach(type => {
    getAll(type).forEach(e => { 
      if (e.x === 0) e.remove(); 
      else e.x -= 1; 
    });
  });

  if (rawScore % 2 === 0) {
    getAll(cloud).forEach(o => { 
      if (o.x === 0) o.remove();
      else o.x -= 1; 
    });
  }

  spawnTimer -= 1;
  if (spawnTimer <= 0) {
    if (displayScore > 50 && Math.random() < 0.35) {
      addSprite(15, groundY, Math.random() < 0.5 ? h1 : b1);
    } else {
      addSprite(15, groundY, Math.random() < 0.5 ? c2 : c1); 
    }
    let speedFactor = Math.floor(displayScore / 100);
    spawnTimer = Math.max(7, Math.floor(Math.random() * 5) + 9 - speedFactor); 
  }
  
  if (Math.random() < 0.05) addSprite(15, Math.floor(Math.random() * 3), cloud);

  let hitCactus = getAll(c1).concat(getAll(c2)).some(c => c.x === d.x && c.y === d.y);
  let hitLowBird = getAll(b1).concat(getAll(b2)).some(b => b.x === d.x && b.y === d.y);
  let isDucking = (d.type === u1 || d.type === u2);
  let hitHighBird = getAll(h1).concat(getAll(h2)).some(b => b.x === d.x && b.y === d.y && !isDucking);
  
  if (hitCactus || hitLowBird || hitHighBird) {
    gameOver = true;
    d.type = d1; 
    
    // Stop the background music when you die!
    if (bgmLoop) {
      bgmLoop.end();
      isMusicPlaying = false;
    }
    playSound(sfxCrash);

    addText("G A M E  O V E R", { x: 2, y: 7, color: color`0` }); 
    addText("- Press J -", { x: 5, y: 12, color: color`7` });
    addText("Jump:W", { x: 2, y: 3, color: color`9` });
    addText("Duck:S", { x: 12, y: 3, color: color`4` });
    return; 
  }

  let currentSpeed = Math.max(50, 110 - Math.floor(rawScore / 35));
  gameTimer = setTimeout(tick, currentSpeed);
}

// --- CONTROLS ---

const handleJump = () => {
  if (gameOver) return;
  
  // NEW: Start background music on the very first jump!
  if (!isMusicPlaying && bgm && bgm.trim() !== "") {
    isMusicPlaying = true;
    // Passing 'Infinity' loops the tune forever
    bgmLoop = playTune(bgm, Infinity); 
  }
  
  if (!isJumping) {
    isJumping = true;
    jumpFrame = 0;
    duckTimer = 0; 
    playSound(sfxJump); 
  }
};
onInput("w", handleJump);
onInput("i", handleJump);

const handleDuck = () => {
  if (gameOver) return;
  if (isJumping) {
    jumpFrame = jumpArc.length; 
  } 
  duckTimer = 8; 
};
onInput("s", handleDuck);
onInput("k", handleDuck);

onInput("j", () => {
  if (gameOver) startGame();
});

startGame();